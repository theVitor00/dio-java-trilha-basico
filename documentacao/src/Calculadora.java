public class Calculadora {
    /**
     * Este metodo utilizado para somar dois numeros inteiros
     * @param numeroUm este e o primeiro parametro do metodo
     * @param numeroDois este e o segundo parametro do metodo
     * @return int o resultado deste metodo e a soma dos  dois numeros.
     */
    public int somar(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }
}
