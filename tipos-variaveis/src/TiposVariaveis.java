public class TiposVariaveis {

    public static void main(String[] args) {

        byte idade = 123;
        short ano = 2021;
        int cep = 21070333;
        long cpf = 98765432109L;
        float pi = 3.14F;
        double salarioMinimo = 1275.33;

        System.out.printf("IDADE: %d%n", idade);
        System.out.printf("ANO:%d%n", ano);
        System.out.printf("CEP: %d%n", cep);
        System.out.printf("CPF: %d%n", cpf);
        System.out.printf("PI: %f%n", pi);
        System.out.printf("SALARIO MINIMO: %f%n", salarioMinimo);
    }
}
