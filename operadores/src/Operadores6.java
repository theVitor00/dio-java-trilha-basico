public class Operadores6 {

    public static void main (String[] args) {

        boolean cond1 = true;
        boolean cond2 = false;

        if (cond1 && cond2) {
            System.out.println("As duas condições são verdadeiras.");
        }

        if (cond1 || cond2) {
            System.out.println("Uma das condições é verdadeira.");
        }

        System.out.println("FIM!");

    }
}
